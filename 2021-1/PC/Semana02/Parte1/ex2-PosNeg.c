/*Escreva um programa para ler um valor e escrever se é positivo ou negativo. Considere o
valor zero como positivo.*/
#include <stdlib.h>
#include <stdio.h>
int main(){
	int num;
		printf("Digite um número: ");
		scanf("%d",&num);
		if(num>=0){printf("POSITIVO");}
		else printf("NEGATIVO");
			
	
	
return 0;	
}

